int		ft_is_prime(int nb)
{
	int i;
	int k;

	i = 1;
	k = 0;
	while (i <= nb)
	{
		if (nb % i == 0)
			k++;
		i++;
	}
	if (k == 2)
		return (1);
	return (0);
}
